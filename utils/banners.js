/**
 * Fonction helper pour afficher une bannière avec couleur
 * @param {string} category - Catégorie de bannière (ex: 'byMisterBeniR', 'lazyCommit')  
 * @param {string} style - Style de la bannière (ex: 'standard', 'big', 'slant', 'compact')
 * @param {string} color - Code couleur ANSI (optionnel, défaut: bleu)
 */
function showBanner(category, style = 'standard', color = '\\x1b[34m') {
  const banner = banners[category]?.[style];
  if (banner) {
    console.log(color + '%s\\x1b[0m', banner);
  } else {
    console.error(`Bannière non trouvée: ${category}.${style}`);
  }
}



